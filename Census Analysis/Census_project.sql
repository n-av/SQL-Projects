-- State wise Total males and females 

select 
 d.state ,
 sum(d.males),
 sum(d.females)
 from
        (select
         c.state ,
		 round(c.population /(c.sex_ratio+1),0) as males , 
		 round((c.population*c.sex_ratio) /(c.sex_ratio+1),0) as females 
		 from
				(select 
				a.State,
				a.sex_ratio /1000 as sex_ratio ,
				b.population 
				from census_one a inner join census_two b on a.District=b.District
			)c	
				)d 
 group by d.state;
 
 
-- state wise , district wise total literacy rate


select 
d.state , 
d.district,
sum(d.literate_people) as total_literate,
sum(d.illiterate_people) as total_illiterate
from
(
		select 
		c.state, 
		c.district,
		round((c.literacy_ratio * c.population),0) as literate_people,
		round((1-c.literacy_ratio)* c.population,0) as illiterate_people
		from 
				( select 
				a.state , 
				a.District ,
				a.literacy/100 as literacy_ratio ,
				b.population 
				from 
				census_one a join census_two b on a.District = b.District 
					)c 
						) d 
group by d.state; 


-- population in previous census and current census

select 
sum(e.previous_census_population) previous_census_population,
sum(e.current_census_population) current_census_population 
from(
        select 
        d.state,
        sum(d.previous_census_population) previous_census_population,
        sum(d.current_census_population) current_census_population 
        from                  
			   (select 
                c.district,
                c.state,
                round(c.population/(1+c.growth),0) previous_census_population,
                c.population current_census_population 
                from                    
						(select 
						a.district,
						a.state,
						a.growth growth,
						b.population 
						from census_one a inner join census_two b on a.district = b.district
						 )c
                           )d 
                            group by d.state
                               )e;
                               
                               
                               
